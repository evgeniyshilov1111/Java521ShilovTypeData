public class TaskEight {
    public static void main(String[] args) {
        //todo 8.	Даны два ненулевых числа. Найти сумму, разность, произведение и частное их квадратов.
        double a = 120;
        double b = 60;
        double s = a + b;
        double r = a - b;
        double p = a * b;
        double x = (a * a) / (b * b);
        System.out.println("сумма = " + s);
        System.out.println("разность = " + r);
        System.out.println("произведение = " + p);
        System.out.println("частное квадратов = " + x);
    }
}
